func CreateCreateasharedlinegroupTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("post_phone_shared_line_groups",
		mcp.WithDescription("Create a shared line group"),
		mcp.WithString("description", mcp.Description("Input parameter: Provide a description for the shared line group.")),
		mcp.WithString("display_name", mcp.Required(), mcp.Description("Input parameter: Provide a name to help identify the shared line group.")),
		mcp.WithNumber("extension_number", mcp.Description("Input parameter: Extension number to be assigned to the shared line group. If a [site code has been assigned](https://support.zoom.us/hc/en-us/articles/360020809672#h_79ca9c8f-c97b-4486-aa59-d0d9d31a525b) to the site, provide the short extension number ")),
		mcp.WithString("site_id", mcp.Description("Input parameter: Unique identifier of the [site](https://support.zoom.us/hc/en-us/articles/360020809672-Managing-multiple-sites) that you would like to use for the shared line group. You will only be able to add members that belong to this site to the shared line group.\n\nThis field is required only if the [multiple sites](https://support.zoom.us/hc/en-us/articles/360020809672-Managing-multiple-sites) option has been enabled for the account.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    CreateasharedlinegroupHandler(cfg),
	}
}
