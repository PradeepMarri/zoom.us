func CreateAddbatchwebinarregistrantsTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("post_webinars_webinarId_batch_registrants",
		mcp.WithDescription("Perform batch registration"),
		mcp.WithString("webinarId", mcp.Required(), mcp.Description("Unique identifier of the webinar.")),
		mcp.WithBoolean("auto_approve", mcp.Description("Input parameter: If a meeting was scheduled with approval_type `1` (manual approval), but you would like to automatically approve the registrants that are added via this API, you can set the value of this field to `true`. \n\nYou **cannot** use this field to change approval setting for a meeting  that was originally scheduled with approval_type `0` (automatic approval).")),
		mcp.WithArray("registrants", mcp.Description("")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    AddbatchwebinarregistrantsHandler(cfg),
	}
}
